/// Cross-registry integrity check for NPC, dialogue, quest, and heal-location content.
///
/// Checks (in deterministic order for proof-of-teeth isolation):
/// 1. Unique NPC ids (u32)
/// 2. Each NPC's zone_id references an existing zone
/// 3. Each NPC's dialogue_tree_id references an existing tree
/// 4. Each DialogueTree has ≥1 node AND root_node_id exists in that tree's nodes
/// 5. Unique dialogue tree ids
/// 6. Each StartQuest effect references an existing quest id
///
/// 6b. Each GrantItem effect (in auto_effects and choice effects) references an existing item id
///
/// 6c. Each GrantItem-bearing node/choice is guarded by a once-only gate: a `NotFlag(f)` in
///    the same node's `entry_conditions` (or choice's `conditions`) AND a `SetFlag(f)` in
///    the same node's `auto_effects` (or choice's `effects`) — same flag name required.
///    This prevents unlimited item farming via `talk()` re-entry (ADR-0068, 13.5f-1).
///
/// 7. Each QuestDef has ≥1 step
/// 8. Each quest Collect step's item_id references an existing item
/// 9. Each quest reward item_id references an existing item
/// 10. Unique heal location ids
/// 11. Each heal location's zone_id references an existing zone
/// 12. Each heal location's cost_item_id (when Some) references an existing item
pub fn validate_npc_content(
    npcs: &[NpcDef],
    dialogue_trees: &[crate::dialogue::DialogueTree],
    quests: &[crate::quest::QuestDef],
    zones: &[ZoneDef],
    items: &[ItemDef],
    heal_locations: &[HealLocationDef],
) -> Result<(), String> {
    use std::collections::BTreeSet;

    let zone_ids: BTreeSet<u32> = zones.iter().map(|z| z.id).collect();
    let tree_ids: BTreeSet<&str> = dialogue_trees.iter().map(|t| t.id.as_str()).collect();
    let quest_ids: BTreeSet<&str> = quests.iter().map(|q| q.id.as_str()).collect();
    let item_ids: BTreeSet<u32> = items.iter().map(|i| i.id).collect();
    let npc_str_ids: BTreeSet<&str> = npcs.iter().map(|n| n.npc_id.as_str()).collect();

    // 1. Unique NPC ids (u32 numeric) and npc_id strings
    let mut seen_npc_ids: BTreeSet<u32> = BTreeSet::new();
    let mut seen_npc_str_ids: BTreeSet<&str> = BTreeSet::new();
    for npc in npcs {
        if !seen_npc_ids.insert(npc.id) {
            return Err(format!("duplicate NPC id {}", npc.id));
        }
        if !seen_npc_str_ids.insert(npc.npc_id.as_str()) {
            return Err(format!("duplicate NPC npc_id '{}'", npc.npc_id));
        }
    }

    // 2. NPC zone_id cross-ref
    for npc in npcs {
        if !zone_ids.contains(&npc.zone_id) {
            return Err(format!(
                "NPC '{}' references unknown zone id {}",
                npc.npc_id, npc.zone_id
            ));
        }
    }

    // 3. NPC dialogue_tree_id cross-ref
    for npc in npcs {
        if !tree_ids.contains(npc.dialogue_tree_id.as_str()) {
            return Err(format!(
                "NPC '{}' references unknown dialogue_tree_id '{}'",
                npc.npc_id, npc.dialogue_tree_id
            ));
        }
    }

    // 4. Each DialogueTree has ≥1 node, unique node ids, root_node_id exists, next_node refs resolve
    for tree in dialogue_trees {
        if tree.nodes.is_empty() {
            return Err(format!("dialogue tree '{}' has no nodes", tree.id));
        }
        let node_ids: BTreeSet<&str> = tree.nodes.iter().map(|n| n.id.as_str()).collect();
        if node_ids.len() != tree.nodes.len() {
            return Err(format!(
                "dialogue tree '{}' has duplicate node ids",
                tree.id
            ));
        }
        if !node_ids.contains(tree.root_node_id.as_str()) {
            return Err(format!(
                "dialogue tree '{}' root_node_id '{}' not found in nodes",
                tree.id, tree.root_node_id
            ));
        }
        for node in &tree.nodes {
            for choice in &node.choices {
                if let Some(next) = &choice.next_node {
                    if !node_ids.contains(next.as_str()) {
                        return Err(format!(
                            "dialogue tree '{}' node '{}' choice next_node '{}' not found in nodes",
                            tree.id, node.id, next
                        ));
                    }
                }
            }
        }
    }

    // 5. Unique dialogue tree ids
    let mut seen_tree_ids: BTreeSet<&str> = BTreeSet::new();
    for tree in dialogue_trees {
        if !seen_tree_ids.insert(tree.id.as_str()) {
            return Err(format!("duplicate dialogue tree id '{}'", tree.id));
        }
    }

    // 6. StartQuest effects reference existing quests
    for tree in dialogue_trees {
        for node in &tree.nodes {
            for effect in node
                .auto_effects
                .iter()
                .chain(node.choices.iter().flat_map(|c| c.effects.iter()))
            {
                if let crate::dialogue::DialogueEffect::StartQuest(qid) = effect {
                    if !quest_ids.contains(qid.as_str()) {
                        return Err(format!(
                            "dialogue tree '{}' StartQuest effect references unknown quest '{}'",
                            tree.id, qid
                        ));
                    }
                }
            }
        }
    }

    // 6b. GrantItem effects must reference items that exist in the items registry.
    for tree in dialogue_trees {
        for node in &tree.nodes {
            for effect in node
                .auto_effects
                .iter()
                .chain(node.choices.iter().flat_map(|c| c.effects.iter()))
            {
                if let crate::dialogue::DialogueEffect::GrantItem(item_id, _) = effect {
                    if !item_ids.contains(item_id) {
                        return Err(format!(
                            "dialogue tree '{}' GrantItem effect references unknown item id {}",
                            tree.id, item_id
                        ));
                    }
                }
            }
        }
    }

    // 6c. Once-only gate: every GrantItem-bearing node/choice must have a
    // Condition::NotFlag(flag) + DialogueEffect::SetFlag(flag) pair for the SAME flag.
    //
    // Rationale: `talk` re-applies auto_effects on EVERY call (find_entry_node
    // re-runs each time). A GrantItem without this gate is an unlimited item farm.
    // The pairing must use the SAME flag name — a mismatched (NotFlag("a"),SetFlag("b"))
    // does NOT close the loop. See ADR-0068 and npc.rs `talk` comment.
    let once_only_gated = |conditions: &[crate::dialogue::Condition],
                           effects: &[crate::dialogue::DialogueEffect]|
     -> bool {
        let has_grant = effects
            .iter()
            .any(|e| matches!(e, crate::dialogue::DialogueEffect::GrantItem(_, _)));
        if !has_grant {
            return true;
        }
        let not_flags: BTreeSet<&str> = conditions
            .iter()
            .filter_map(|c| match c {
                crate::dialogue::Condition::NotFlag(f) => Some(f.as_str()),
                _ => None,
            })
            .collect();
        let set_flags: BTreeSet<&str> = effects
            .iter()
            .filter_map(|e| match e {
                crate::dialogue::DialogueEffect::SetFlag(f) => Some(f.as_str()),
                _ => None,
            })
            .collect();
        not_flags.intersection(&set_flags).next().is_some()
    };
    for tree in dialogue_trees {
        for node in &tree.nodes {
            if !once_only_gated(&node.entry_conditions, &node.auto_effects) {
                return Err(format!(
                    "dialogue tree '{}' node '{}' grants an item via auto_effects without \
                     a NotFlag+SetFlag once-only gate — talk() re-applies auto_effects \
                     every call, making this an unlimited item farm",
                    tree.id, node.id
                ));
            }
            for (ci, choice) in node.choices.iter().enumerate() {
                if !once_only_gated(&choice.conditions, &choice.effects) {
                    return Err(format!(
                        "dialogue tree '{}' node '{}' choice {} grants an item without \
                         a NotFlag+SetFlag once-only gate on that choice",
                        tree.id, node.id, ci
                    ));
                }
            }
        }
    }

    // 7. Each QuestDef has ≥1 step
    for quest in quests {
        if quest.steps.is_empty() {
            return Err(format!("quest '{}' has no steps", quest.id));
        }
    }

    // 8. Quest step cross-refs: Collect item_id + Talk npc_id
    for quest in quests {
        for step in &quest.steps {
            match &step.trigger {
                crate::quest::StepTrigger::Collect { item_id, .. } => {
                    if !item_ids.contains(item_id) {
                        return Err(format!(
                            "quest '{}' Collect step references unknown item id {item_id}",
                            quest.id
                        ));
                    }
                }
                crate::quest::StepTrigger::Talk { npc_id } => {
                    if !npc_str_ids.contains(npc_id.as_str()) {
                        return Err(format!(
                            "quest '{}' Talk step references unknown npc_id '{npc_id}'",
                            quest.id
                        ));
                    }
                }
                crate::quest::StepTrigger::Defeat { .. } => {
                    // species cross-ref deferred until Defeat quests are added to content
                }
            }
        }
    }

    // 9. Quest reward item cross-ref
    for quest in quests {
        for reward_item in &quest.reward.items {
            if !item_ids.contains(&reward_item.item_id) {
                return Err(format!(
                    "quest '{}' reward references unknown item id {}",
                    quest.id, reward_item.item_id
                ));
            }
        }
    }

    // 10. Unique heal location ids
    let mut seen_heal_ids: BTreeSet<u32> = BTreeSet::new();
    for hl in heal_locations {
        if !seen_heal_ids.insert(hl.location_id) {
            return Err(format!("duplicate heal location id {}", hl.location_id));
        }
    }

    // 11. Heal location zone_id cross-ref
    for hl in heal_locations {
        if !zone_ids.contains(&hl.zone_id) {
            return Err(format!(
                "heal location {} references unknown zone id {}",
                hl.location_id, hl.zone_id
            ));
        }
    }

    // 12. Heal location cost_item_id cross-ref (when Some)
    for hl in heal_locations {
        if let Some(cost_id) = hl.cost_item_id {
            if !item_ids.contains(&cost_id) {
                return Err(format!(
                    "heal location {} cost_item_id {} references unknown item",
                    hl.location_id, cost_id
                ));
            }
        }
    }

    // 13. Heal location cost coherence: cost_item_id with cost_qty == 0 is a config error
    for hl in heal_locations {
        if hl.cost_item_id.is_some() && hl.cost_qty == 0 {
            return Err(format!(
                "heal location {} has cost_item_id set but cost_qty is 0",
                hl.location_id
            ));
        }
    }

    Ok(())
}

