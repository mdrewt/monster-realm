/// A warp definition: a tile in one zone that transports the player to another
/// zone (M11a). Warps are stored as an overlay list on `TileMap`, not as tile
/// glyphs, so the walkability grid stays clean (ADR-0065).
///
/// `WarpDef` derives `Serialize` so it flows through the `zone_map()` wasm
/// export (M11c intentional ABI).
#[derive(Debug, Clone, PartialEq, Eq, serde::Serialize, serde::Deserialize)]
pub struct WarpDef {
    /// The tile in this zone that triggers the warp (must be walkable).
    pub from: TilePos,
    /// The destination zone id.
    pub to_zone: u32,
    /// The landing tile in the destination zone (must be walkable).
    pub to_tile: TilePos,
}

/// A zone map definition: string-art tile rows + warp list, keyed by zone_id.
/// Loaded from `content/zone_maps/*.ron` by `load_zone_maps()`.
#[derive(Debug, Clone, serde::Serialize, serde::Deserialize)]
pub struct ZoneMapDef {
    pub zone_id: u32,
    /// String-art rows: `'.'` = floor, `'#'` = wall, `'~'` = tall grass.
    pub rows: Vec<String>,
    /// Warps from this zone. Defaults to empty if absent in RON.
    #[serde(default)]
    pub warps: Vec<WarpDef>,
}

